	export default {
		data() {
			return {
				ruleForm: {
												caipinmingcheng: '',
																caipinfenlei: '',
																tupian: '',
																caipinjiage: '',
																cailiao: '',
																fenliang: '',
																caipinjieshao: '',
																clicktime: '',
																clicknum: '',
												},
																				caipinfenleiOptions: [],
				caipinfenleiIndex: 0,
																																																																				// 登陆用户信息
				user: {},
                                ro:{
                                   caipinmingcheng : false,
                                   caipinfenlei : false,
                                   tupian : false,
                                   caipinjiage : false,
                                   cailiao : false,
                                   fenliang : false,
                                   caipinjieshao : false,
                                   clicktime : false,
                                   clicknum : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																												
																		// 下拉框
			res = await this.$api.option(`caipinfenlei`,`caipinfenlei`,{});
			this.caipinfenleiOptions = res.data;
																																																			
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`caipinxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='caipinmingcheng'){
					this.ruleForm.caipinmingcheng = obj[o];
					this.ro.caipinmingcheng = true;
					continue;
					}
					if(o=='caipinfenlei'){
					this.ruleForm.caipinfenlei = obj[o];
					this.ro.caipinfenlei = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='caipinjiage'){
					this.ruleForm.caipinjiage = obj[o];
					this.ro.caipinjiage = true;
					continue;
					}
					if(o=='cailiao'){
					this.ruleForm.cailiao = obj[o];
					this.ro.cailiao = true;
					continue;
					}
					if(o=='fenliang'){
					this.ruleForm.fenliang = obj[o];
					this.ro.fenliang = true;
					continue;
					}
					if(o=='caipinjieshao'){
					this.ruleForm.caipinjieshao = obj[o];
					this.ro.caipinjieshao = true;
					continue;
					}
					if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					this.ro.clicktime = true;
					continue;
					}
					if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					this.ro.clicknum = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																												
			// 多级联动参数
																																																												
																																																									
																																																			// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},
												
															// 下拉变化
			caipinfenleiChange(e) {
				this.caipinfenleiIndex = e.target.value
				this.ruleForm.caipinfenlei = this.caipinfenleiOptions[this.caipinfenleiIndex]
			},
																																																
																											tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																															
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																if((!this.ruleForm.caipinmingcheng)){
					this.$utils.msg(`菜品名称不能为空`);
					return
				}
																																								if((!this.ruleForm.caipinfenlei)){
					this.$utils.msg(`菜品分类不能为空`);
					return
				}
																																								if((!this.ruleForm.tupian)){
					this.$utils.msg(`图片不能为空`);
					return
				}
																																								if((!this.ruleForm.caipinjiage)){
					this.$utils.msg(`菜品价格不能为空`);
					return
				}
												if(this.ruleForm.caipinjiage&&(!this.$validate.isIntNumer(this.ruleForm.caipinjiage))){
					this.$utils.msg(`菜品价格应输入整数`);
					return
				}
																																																																																																																																																																								if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
																																if(this.ruleForm.id){
					await this.$api.update(`caipinxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`caipinxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

